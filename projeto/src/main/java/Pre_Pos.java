
public class Pre_Pos {
    public static void main(String[] args) {
        
        //pre incremento
        
        int a = 10;
        int b = ++a;
        
        //p√≥s incremento
        
        int c = 10;
        int d = c++;
        
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        
    }
}
